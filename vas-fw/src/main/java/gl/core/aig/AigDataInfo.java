package gl.core.aig;




public class AigDataInfo
{
  private String id = null;
  private String subId = null;
  private Object config = null;
  private Object data = null;






  
  public final String getId() { return this.id; }





  
  public final String getSubId() { return this.subId; }





  
  public final Object getConfig() { return this.config; }





  
  public final Object getData() { return this.data; }





  
  public final void setId(String id) { this.id = id; }





  
  public final void setSubId(String subId) { this.subId = subId; }





  
  public final void setConfig(Object config) { this.config = config; }





  
  public final void setData(Object data) { this.data = data; }
}
